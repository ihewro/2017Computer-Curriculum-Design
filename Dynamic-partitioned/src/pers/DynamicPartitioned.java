    public void partionRecovery(String taskName){
        int flag = 0;
        int size = partitions.size();
        for(int i = 0; i< partitions.size(); i++,flag++){
            System.out.println("这是第" + i + "个分区");
            System.out.println("当前分区的作业名称" + partitions.get(i).getTask().getTaskName() + "             输入的作业名称是" + taskName);
            if (partitions.get(i).getTask().getTaskName().equals(taskName)) {
                int front = 0;
                int back = 0;
                if (i != 0){
                    System.out.println(i + "  不是第一个分区");
                    if (partitions.get(i-1).getStatus() == 0){
                        System.out.println("前一个分区为空");
                        front = 1;
                    }
                }
                if (i != partitions.size()-1){
                    System.out.println(i + "  不是最后一个分区");
                    if (partitions.get(i+1).getStatus() == 0) {
                        System.out.println("后一个分区为空");
                        back = 1;
                    }
                }
                if (front == 1 && back == 1) {
                    Partition newPartition = new Partition();
                    newPartition.setforAll(partitions.get(i-1).getStartAdress(), partitions.get(i-1).getSize() +partitions.get(i).getSize() + partitions.get(i+1).getSize(), 0, nullTask);
                    partitions.set(i -1, newPartition);
                    partitions.remove(i+1);
                    partitions.remove(i);
                }else if (front == 0 && back == 1){
                    Partition newPartition = new Partition();
                    newPartition.setforAll(partitions.get(i).getStartAdress(), partitions.get(i).getSize() +partitions.get(i+1).getSize(), 0, nullTask);
                    partitions.set(i, newPartition);
                    partitions.remove(i+1);
                }else if (front ==1 && back == 0){
                    Partition newPartition = new Partition();
                    newPartition.setforAll(partitions.get(i-1).getStartAdress(), partitions.get(i-1).getSize() + partitions.get(i).getSize(), 0, nullTask);
                    partitions.set(i-1, newPartition);
                    partitions.remove(i);
                }else if (front == 0 && back ==0){
                    Partition newPartition = new Partition();
                    newPartition.setforAll(partitions.get(i).getStartAdress(), partitions.get(i).getSize(), 0, nullTask);
                    partitions.set(i, newPartition);
                } 
                break;
            }
        }//end of for circle 
        if (flag >= size){
            System.err.println("没有该作业名称，请输入正确的作业ID:" + taskName);
            jDialog4.setVisible(true);
        }
        MyTableModel.reloadJTable(dtm);
    }
    
