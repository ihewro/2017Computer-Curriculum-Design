    void partionAcllocation(Task task,int methodChoice){
        System.out.println("正确进入分配内存2");
        Partition nextPartition = new Partition();
        if(methodChoice == 1){
            SortByPartitionSize SortByPartitionSize = new SortByPartitionSize();
            Collections.sort(partitions,SortByPartitionSize);//将所有分区按照分区大小排序
        }
        for(int i=0; i< partitions.size();i++){
            System.out.print("(分配前)第" + i + "个分区的大小为" + currentPartition.getSize());
            for (int j = 1; j<5; j++) {
                System.out.print("(分配前)第" + j + "位置:" + partitions.get(i).getByIndex(j) + "   ");
            }
            System.out.println("");
        }
        int size = partitions.size();//必须是之前的分区个数
        int flag = 0;
        for(int i=0; i < partitions.size();i++,flag++){
            currentPartition = partitions.get(i);
            if(currentPartition.status == 0 && currentPartition.getSize() >= task.getSize()){
                
                System.out.println("");
                System.out.print("当前分区信息：");
                for(int k=0; k< 5; k++){
                    System.out.print("第" + k +"位置：" + currentPartition.getByIndex(k));
                }
                System.out.println("");
                
                int result = currentPartition.getStartAdress() + task.getSize();
                
                System.out.println("当前分区+作业大小:   " + result + "    当前分区大小:   " + currentPartition.getStartAdress() + "   作业大小:   "+ task.getSize());
                                
                nextPartition.setforAll(currentPartition.getStartAdress() + task.getSize(),
                    currentPartition.size - task.size, 0, nullTask);
                currentPartition.setforAll(currentPartition.getStartAdress(), 
                    task.getSize(), 1, task);
                
                System.out.println("");
                
               
                
                System.out.print("下一个插入的信息：");
                for(int k=0; k< 5; k++){
                    System.out.print("第" + k +"位置：" + nextPartition.getByIndex(k));
                }
                System.out.println("");

                System.out.println("");
                System.out.print("当前分区信息（修改后）：");
                for(int k=0; k< 5; k++){
                    System.out.print("第" + k +"位置：" + currentPartition.getByIndex(k));
                }
                System.out.println("");
                
                
                partitions.set(i, currentPartition);
                if(nextPartition.getSize()!=0){
                    partitions.add(i+1, nextPartition);    
                }
                //MyTableModel.reloadJTable(dtm);
                break;
            }
        }//end of for circle 
        if (flag >= size) {
            System.err.println("内存空间不够！");
            jDialog1.setVisible(true);
        }
        MyTableModel.reloadJTable(dtm);
    }
    
